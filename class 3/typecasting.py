#type casting
name = "chico"
print("the data type for name is",type(name))
age = "37"
print("the data type for age is",type(age))
height = 193.01
print("the data type for height is",type(height))
is_student = True
print("the data type for is_student is",type(is_student))
age = bool(age)
print(age)
print("the data type for age is",type(age))
height = int(height)
print(height)
print("the data type for height is",type(height))
is_student = str(is_student)
print(is_student)
print("the type of is_student",type(is_student))